package printIndexAndElement;

public class PrintIndexAndElement {

    public static void main(String[] args) {

        int[] intArray = new int[] {1, 4, 2, 7, 9, 1, 2, 3, 4, 5};

        for (int i = 0; i < intArray.length; i++) {
            System.out.println(i + " - " + intArray[i]);
        }
    }
}
